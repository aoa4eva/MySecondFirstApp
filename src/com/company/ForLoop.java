package com.company;

public class ForLoop {
    public static void main(String[] args) {
//        The for loop uses discrete values to start and end.
        /* For example, you can have a loop going from 0 to 10 in increments of 1*/
        for(int counter=0; counter<=10; counter++)
        {
            System.out.println(counter);
        }

    }
}
